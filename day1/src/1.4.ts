// Data types in Typescript 

// Primitive and non primitive


// Primitive Data types

// string
let FirstName : string = 'Turan';  

// number
let money : number = 0;

// boolean
let job : boolean = false;

// undefined
let x : undefined = undefined;

// null
let y : null = null;



// Non-primitive Data types

// Array

let friends : string[] = ['Chandler', 'Ross', 'Joey'];

let eligibleRole : number[] = [1,2,3];


// Tuple --> one kind of array --> which maintains order according to --> types of values

let nameAge : [number,string] = [69,'Johnny'];

